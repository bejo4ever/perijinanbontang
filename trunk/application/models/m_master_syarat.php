<?php
class M_master_syarat extends App_Model{
	var $mainSql = "SELECT 
				ID_SYARAT,
				NAMA_SYARAT
				FROM master_syarat 
			WHERE ID_SYARAT IS NOT NULL 
	";
	
	function __construct(){
        parent::__construct();
        $this->table_name = 'master_syarat';
        $this->column_primary = 'ID_SYARAT';
        $this->column_order = '';
		$this->column_unique = '';
    }
	
	function getList($params){
		extract($params);
		$sql = $this->mainSql;
		if(@$searchText != ''){
			$sql .= "
				AND (
					NAMA_SYARAT LIKE '%".$searchText."%'
					)
			";
		}
				if(@$limit_start != 0 && @$limit_start != 0){
			$sql .= " LIMIT ".@$limit_start.", ".@$limit_end." ";
		}
		$result = $this->__listCore($sql, $params);
		return $result;
	}
	
	function search($params){
		extract($params);
		
		$sql = $this->mainSql;
		
		if(@$NAMA_SYARAT != ''){
			$sql .= " AND NAMA_SYARAT LIKE '%".$NAMA_SYARAT."%' ";
		}
		if(@$limit_start != 0 && @$limit_start != 0){
			$sql .= " LIMIT ".@$limit_start.", ".@$limit_end." ";
		}
		$result = $this->__listCore($sql, $params);
		return $result;
	}
	
	function printExcel($params){
		extract($params);
		if(@$currentAction == "GETLIST"){
			$result = $this->getList($params);
		}else if(@$currentAction == "SEARCH"){
			$result = $this->search($params);
		}
		return $result;
	}
	
}